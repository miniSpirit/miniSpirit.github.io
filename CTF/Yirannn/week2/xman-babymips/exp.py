tar = [0, 0, 0, 0, 0, 0x52, 0xFD, 0x16, 0xA4, 0x89, 0xBD, 0x92, 0x80, 0x13, 0x41, 0x54, 0xA0, 0x8D, 0x45, 0x18, 0x81, 0xDE, 0xFC, 0x95, 0xF0, 0x16, 0x79, 0x1A, 0x15, 0x5B, 0x75, 0x1F, 0x00]
sta = "qctf{"
for i in range(5, 32) :
    for bf in range(256) :
        tmp = bf
        tmp ^= 32-i
        if i&1 :
            tmp = ((tmp >> 2) | (tmp << 6))&0xff
        else :
            tmp = ((tmp << 2) | (tmp >> 6))&0xff
        if tmp == tar[i] :
            sta += chr(bf)
            break
print(sta)
